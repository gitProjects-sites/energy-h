<h2>1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Общие положения</h2>
<p>
	 1.1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; В целях выполнения норм действующего законодательства Российской Федерации в полном объеме OOO «Эйч Энерджи» считает важнейшими своими задачами соблюдение принципов законности, справедливости и конфиденциальности при обработке персональных данных, а также обеспечение безопасности процессов их обработки.
</p>
<p>
	 1.2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Настоящая Политика об организации обработки и обеспечении безопасности персональных данных характеризуется следующими признаками:
</p>
<p>
	 (1)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; разработана в целях реализации требований действующего законодательства Российской Федерации в области обработки и защиты персональных данных;
</p>
<p>
	 (2)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; раскрывает способы и принципы обработки OOO «Эйч Энерджи» персональных данных, права и обязанности OOO «Эйч Энерджи» при обработке персональных данных, права субъектов персональных данных, а также включает перечень мер, применяемых OOO «Эйч Энерджи» в целях обеспечения безопасности персональных данных при их обработке;
</p>
<p>
	 (3)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; является общедоступным документом, декларирующим концептуальные основы деятельности <br>
	 OOO «Эйч Энерджи» при обработке и защите персональных данных.
</p>
<p>
	 1.3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; OOO «Эйч Энерджи» до начала обработки персональных данных осуществило уведомление уполномоченного органа по защите прав субъектов персональных данных о своем намерении осуществлять обработку персональных данных. OOO «Эйч Энерджи» добросовестно и в соответствующий срок осуществляет актуализацию сведений, указанных в уведомлении.
</p>
