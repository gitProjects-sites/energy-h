<h2>4.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Принципы, цели, содержание и способы обработки ПДн</h2>
<p>
	 4.1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Оператор в своей деятельности обеспечивает соблюдение принципов обработки ПДн, указанных в ст. 5 Федерального закона от 27.07.2006 № 152-ФЗ «О персональных данных».
</p>
<p>
	 4.2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Оператор осуществляет сбор и дальнейшую обработку ПДн в следующих целях:
</p>
<p>
	 (1)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; замещение вакантных должностей (трудоустройство) соискателями, наиболее полно соответствующими требованиям OOO «Эйч Энерджи», а также осуществление информационного и организационного взаимодействия с соискателями;
</p>
<p>
	 (2)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ведение кадрового, бухгалтерского, налогового и воинского учета в отношении работников, принятие управленческих и кадровых решений, контроль над трудовой дисциплиной, выплата заработной платы, премий и возмещений по понесенным расходам, участие в зарплатном проекте, предоставление гарантий, компенсаций, обязательного социального страхования, налоговых вычетов и материальной помощи, направление в командировки;
</p>
<p>
	 (3)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; организация деловых поездок, обучение, инструктаж, проверка знаний;
</p>
<p>
	 (4)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; обеспечение охраны труда, техники безопасности, пожарной безопасности и защиты от чрезвычайных ситуаций, проведение специальной оценки условий труда, выявление нарушений состояния здоровья и медицинских противопоказаний к работе (в том числе в связи с распространением коронавирусной инфекции), проведение медицинских осмотров и медицинских освидетельствований, расследование, учет и оповещение о несчастных случаях;
</p>
<p>
	 (5)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; участие в программах добровольного страхования, общественное признание назначения работника на новую должность, поздравление сотрудников в связи с юбилеем осуществления в компании трудовой функции, оформление новогодних детских подарков;
</p>
<p>
	 (6)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; облегчение и повышение эффективности коммуникаций между работниками и иными лицами, выдача визитных карточек, обеспечение служебной униформой, эксплуатация и использование служебных транспортных средств.
</p>
<p>
	 (7)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; представление интересов Оператора и наделение специальными полномочиями (+оформление доверенностей), участие Оператора в судебных процессах и исполнение судебных актов, независимая проверка бухгалтерской (финансовой) отчетности Оператора, внешний и внутренний контроль бизнес-процессов Оператора, предотвращение и урегулирование конфликта интересов, осуществление делопроизводства;
</p>
<p>
	 (8)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; защита жизни и здоровья физических лиц, имущества и объектов недвижимости от противоправных посягательств, обеспечение внутриобъектового и пропускного режимов на объектах недвижимости <br>
	 OOO «Эйч Энерджи»;
</p>
<p>
	 (9)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; выделение (подключение) средств вычислительной техники и оргтехники, управление доступом к ресурсам информационных систем, сетевой инфраструктуры и систем безопасности, использование и эксплуатация средств вычислительной техники, оргтехники, информационных систем, сетевой инфраструктуры и систем безопасности, предоставление и контроль использования служебных сервисов мобильной радиосвязи и доступа к сети «Интернет»;
